package cmsc131_examples;

public class Conversions {
	public static void main(String[] args) {
		float f = 6;
		int j = (int) 3.14;
		float g = 1/5;
		float h = (float) (1) /5;
		
		System.out.println("f="+f);
		System.out.println("j="+j);
		System.out.println("g="+g);
		System.out.println("h="+h);
	}
}
